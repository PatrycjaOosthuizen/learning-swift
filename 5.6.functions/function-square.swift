/* Write a function square that takes an integer as input and returns its square.*/

func square(of number: Int) -> Int {
    return number * number
}

// Example usage:
print(square(of: 4))  // Output: 16
print(square(of: -3)) // Output: 9


/* Explanation:
The function square takes an integer as input.
It returns the square of the number by multiplying it by itself.
Example calls show the function in action.
